#import "NewsViewController.h"
#import "CountdownViewController.h"
#import "RSSParser.h"
#import "ArticleViewController.h"
#import "CampBrookwoodsAppDelegate.h"

@implementation NewsViewController


#pragma mark -
#pragma mark View lifecycle


- (void)viewDidLoad
{
    [super viewDidLoad];

	self.navigationItem.title = @"News Feed";
    self.navigationItem.leftBarButtonItem = [[[UIBarButtonItem alloc] initWithBarButtonSystemItem:UIBarButtonSystemItemDone target:self action:@selector(returnHome)] autorelease];
	
	//handle RSS feed
	titles = [[[NSMutableArray alloc] initWithCapacity:0] autorelease];
	[titles retain];
	
	articles = [[[NSMutableArray alloc] initWithCapacity:0] autorelease];
	[articles retain];
	
	RSSParser *parser = [[[RSSParser alloc] init] autorelease];
    [parser setDelegate:self];
	[parser parseRSSFeedWithTitleArray:&titles andArticlesArray:&articles];
}

- (void) RSSParserDidComplete
{
    [self.tableView reloadData];
}

